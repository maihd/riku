{
    return bool3(a.x != b.x, a.y != b.y, a.z != b.z);
}

namespace math
{
    inline bool all(const int3& v)
    {
        return v.x != 0 && v.y != 0 && v.z != 0;
    }

    inline bool any(const int3& v)
    {
        return v.x != 0 || v.y != 0 || v.z != 0;
    }
}